    def _gen_itil(self):
        self.add_page_break()
        self.add_heading2("附录II ITIL运维管理流程全集")
        self.add_body_paragraph(
            "我方服务管理体系以ITIL 4为框架，建立事件、问题、变更、配置、发布、服务请求六大流程，"
            "各流程目标、范围、角色、步骤、输入输出、KPI与支撑工具如下，确保服务标准化、可追溯、可改进。")
        for p in ITIL_PROCESSES:
            self.add_heading3(p["name"])
            self.add_body_paragraph(f"目标：{p['objective']}")
            self.add_body_paragraph(f"范围：{p['scope']}")
            self.add_table_from_data(["角色", "职责"], [[r, d] for r, d in p["roles"]])
            self.add_table_from_data(["步骤", "动作", "输出", "时限"], [[a, b, c, e] for a, b, c, e in p["steps"]])
            self.add_body_paragraph(f"输入：{p['inputs']}")
            self.add_body_paragraph(f"输出物：{p['outputs']}")
            self.add_body_paragraph(f"关键指标（KPI）：{p['kpis']}")
            self.add_body_paragraph(f"支撑工具：{p['tool']}")

